function TriangleArea(side, triangleHeight) {
    // Do the calculation in order to get the area of the triangle
    let area = (side * triangleHeight) / 2;

    // Print the result in the console
    console.log(area.toFixed(2));

}

TriangleArea(20, 30);
TriangleArea(15, 35);
TriangleArea(7.75, 8.45);
TriangleArea(1.23456, 4.56789);