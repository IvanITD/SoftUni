function NumbersFromOneToTen() {
    // Creating a variable for later use
    let number = 1;

    // Creating a while loop to go through the numbers from 1 to 10
    while (number <= 10) {
        // Print the result to the console
        console.log(number);

        // Add one more to the number to go to the next one
        number++;
    }
}
NumbersFromOneToTen();